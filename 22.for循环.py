"""
python中的for循环是指，for。。。in。。。和其他语言中的for循环不太一样

语法：
    for 元素 in 可迭代对象
注意：
1. python中的可迭代对象一般指的是字符串、列表，元祖，集合等
2. range是用来生成指定区间的整数序列
   第一个参数表示从几开始，包含这个数
   第二个参数表示到几结束，不包含这个数

"""

"""
举例1：输出从1到10的所有数字
for i in range (1,11):  # 注意这里有一个冒号
    print(i)
"""


"""
举例2：输出列表中的所有元素

for i in [12, 34, 5, 6, 86, 42]:  # 注意这里的冒号
    print(i)
"""

"""
举例3：遍历字符串

for i in "hello":
    print(i)
"""


























